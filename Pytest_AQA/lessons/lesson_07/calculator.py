def addition(first_number: int | float, second_number: int | float) -> int | float:
    result = first_number + second_number
    return result


def add_numbers(a: float | int, b: float | int, c: float | int) -> float | int:
    result = a + b + c
    return result


def power(number: int, base: int) -> int:
    result = number ** base
    return result


def sum_list(numbers: list):
    result = sum(numbers)
    return result
